import React, { useEffect, useState } from 'react';
import { Provider } from "./components/ui/provider"

function App() {
  const [message, setMessage] = useState('');

  useEffect(() => {
    fetch('/api/hello')
      .then((res) => res.json())
      .then((data) => setMessage(data.message));
  }, []);

  return (
    <Provider>
      <h1>{message}</h1>
    </Provider>
  );
}

export default App;
